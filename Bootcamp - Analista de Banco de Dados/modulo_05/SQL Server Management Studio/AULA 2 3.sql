--PEDIDOS DE COMPRA
SELECT C7_NUM, CONVERT(VARCHAR,CONVERT(DATE,C7_EMISSAO),103) C7_PRODUTO, B1_DESC, C7_QUANT, C7_FORNECE, A2_NOME FROM SC7990
INNER JOIN SB1990 ON SC7990.C7_PRODUTO = SB1990.B1_COD 
AND SC7990.C7_FILIAL = SB1990.B1_FILIAL
AND SC7990.D_E_L_E_T_= '' AND SB1990.D_E_L_E_T_ = ''
INNER JOIN SA2990 ON SC7990.C7_FORNECE = SA2990.A2_COD AND SA2990.D_E_L_E_T_ = ''

-- NOTA FISCAL DE COMPRA
SELECT F1_DOC, F1_SERIE, D1_COD, D1_QUANT, F1_EMISSAO, F1_FORNECE, A2_NOME FROM SF1990 SF1
INNER JOIN SD1990 SD1 ON SF1.F1_DOC = SD1.D1_DOC AND SF1.F1_SERIE = SD1.D1_SERIE
AND SF1.F1_FILIAL = SD1.D1_FILIAL

INNER JOIN SA2990 SA2 ON SF1.F1_FORNECE = A2_COD
WHERE SF1.D_E_L_E_T_ <> '*' AND SD1.D_E_L_E_T_ <> '*' AND SA2.D_E_L_E_T_ <> '*'
